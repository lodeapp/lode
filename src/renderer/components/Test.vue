<template>
    <Nugget
        :model="test"
        class="test"
        :class="{ 'is-active': isActive, 'is-child-active': isChildActive }"
        :has-children="hasChildren"
        :handler="onActivate"
        @focus.native="onActivate"
        @contextmenu.native.stop.prevent="onContextMenu"
        @keydown.native.self.stop.prevent.space="onSelectiveClick"
    >
        <template slot="header">
            <div v-if="selectable" class="selective-toggle" :class="{ disabled: running }" @mousedown.prevent.stop="onSelectiveClick">
                <button tabindex="-1" type="button" :disabled="running"></button>
                <input
                    type="checkbox"
                    tabindex="-1"
                    v-model="selected"
                    :disabled="running"
                    @click.prevent
                    @mousedown.prevent
                    @mousedown.stop="selected = !selected"
                >
            </div>
            <div class="test-name" :title="displayName">{{ displayName }}</div>
        </template>
        <template v-if="hasChildren">
            <Test
                v-for="child in test.tests"
                :key="child.id"
                :test="child"
                :running="running"
                :selectable="selectable"
                @open="$emit('open')"
                @activate="onChildActivation"
            />
        </template>
    </Nugget>
</template>

<script>
import { mapGetters } from 'vuex'
import { Menu } from '@main/menu'
import Nugget from '@/components/Nugget'

export default {
    name: 'Test',
    components: {
        Nugget
    },
    props: {
        test: {
            type: Object,
            required: true
        },
        selectable: {
            type: Boolean,
            default: false
        },
        running: {
            type: Boolean,
            default: false
        }
    },
    computed: {
        hasChildren () {
            return ((this.test.tests || []).length > 0)
        },
        isChildActive () {
            return this.context.indexOf(this.test.id) > -1
        },
        selected: {
            get () {
                return this.test.selected
            },
            set (checked) {
                this.test.toggleSelected(checked)
            }
        },
        displayName () {
            return this.test.displayName || this.test.name
        },
        originalName () {
            return this.test.name !== this.displayName ? this.test.name : false
        },
        isActive () {
            if (this.test.id === this.testActive) {
                return true
            }
            this.deactivate()
            return false
        },
        ...mapGetters({
            testActive: 'context/test',
            context: 'context/breadcrumbs'
        })
    },
    mounted () {
        // If test is already active (i.e. persisted contexts) trigger
        // activation sequence.
        if (this.test.id === this.testActive) {
            this.activate()
        }
    },
    methods: {
        onActivate () {
            if (!this.hasChildren && !this.isActive) {
                this.activate()
                return false
            }
        },
        onSelectiveClick (event) {
            if (this.running) {
                return
            }
            this.selected = !this.selected
        },
        onContextMenu (event) {
            new Menu()
                .add({
                    label: __DARWIN__
                        ? 'Copy Test Name'
                        : 'Copy test name',
                    click: () => {
                        this.$root.copyToClipboard(this.displayName)
                    }
                })
                .addIf(this.originalName, {
                    label: __DARWIN__
                        ? 'Copy Original Test Name'
                        : 'Copy original test name',
                    click: () => {
                        this.$root.copyToClipboard(this.originalName)
                    }
                })
                .separator()
                .add({
                    label: __DARWIN__
                        ? 'Open Suite with Default Program'
                        : 'Open suite with default program',
                    click: () => {
                        this.$emit('open')
                    },
                    enabled: this.canOpen()
                })
                .open()
        },
        canOpen () {
            return this.$parent.canOpen()
        },
        activate () {
            this.$el.focus()
            this.$store.commit('context/TEST', this.test.id)
            // @TODO: redo set active
            // this.test.setActive(true)
            setTimeout(() => {
                this.$emit('activate', [this.test])
            })
        },
        deactivate () {
            // @TODO: redo set active
            // this.test.setActive(false)
        },
        onChildActivation (context) {
            context.unshift(this.test)
            this.$store.commit('context/ADD', this.test.id)
            this.$nextTick(() => {
                this.$emit('activate', context)
            })
        }
    }
}
</script>
